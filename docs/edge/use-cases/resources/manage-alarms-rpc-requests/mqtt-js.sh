# Set ThingsBoard Edge host
export TB_EDGE_HOST=YOUR_TB_EDGE_HOST # e.g. REPLACE YOUR_TB_EDGE_HOST with your ThingsBoard Edge server installation IP address or hostname, e.g. 'localhost'
# Set ThingsBoard Edge MQTT port
export TB_EDGE_MQTT_PORT=YOUR_TB_EDGE_MQTT_PORT # REPLACE YOUR_TB_EDGE_MQTT_PORT with your ThingsBoard Edge MQTT port, e.g. 11883 or 1883
# Set Air Conditioner device token
export ACCESS_TOKEN=YOUR_ACCESS_TOKEN # # REPLACE YOUR_ACCESS_TOKEN with your device access token, e.g. 'fSzqLypyZdGxANdhG339'
# Run cooler.js script
node cooler.js
